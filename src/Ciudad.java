
public class Ciudad {
	private String nombre;

	/**
	 * @return the nombre
	 */
	public String getNombre() {
		return nombre;
	}

	/**
	 * @param nombre the nombre to set
	 */
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}

	public Ciudad(String nombre) {
		super();
		this.nombre = nombre;
	}
	
	

}
